#program konversi satuan celcius ke satuan lain

c = float(input("masukan suhu celcius :"))
print("suhu celcius :",c)

#reamur
r = (4/5) * c
print("suhu dalam reamur :",r)

#fahrenheit
f = ((9/5) * c) + 32
print("suhu dalam fahrenheit :",f)

#kelvin
k = c + 273
print("suhu dalam kelvin :",k)

