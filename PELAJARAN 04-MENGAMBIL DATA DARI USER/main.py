data_01 = input("masukan data :")#data yang dimasukan tipe datanya pasti string
print("data :",data_01,type (data_01))

#casting ke integer
data_02 = int(input("masukan angka :"))#jika sudah di casting maka data yang dimasukan harus angka
print("data :",data_02,type(data_02))

#casting ke float
data_03 = float(input("masukan nomor :"))#jika sudah di casting maka data yang dimasukan harus angka
print("data :",data_03,type(data_03))

#casting ke boolean
data_04 = bool(int(input("masukan data :")))#tambahkan int agar bisa terbaca
print("data :",data_04,type(data_04))